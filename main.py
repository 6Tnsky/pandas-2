import pandas as pd
import numpy as np

# 1. Создаем DataFrame с данными
data = {
    'Имя': ['Алексей', 'Мария', 'Иван', 'Ольга', 'Дмитрий', 'Екатерина', 'Сергей', 'Анна', 'Павел', 'Наталья'],
    'Математика': [5, 4, 3, 5, 4, 4, 3, 5, 4, 5],
    'Русский язык': [4, 5, 3, 4, 5, 4, 5, 3, 4, 5],
    'Физика': [3, 4, 5, 4, 3, 5, 4, 5, 4, 3],
    'Химия': [5, 3, 4, 5, 4, 3, 5, 4, 5, 4],
    'Литература': [4, 5, 3, 4, 5, 4, 3, 5, 4, 5]
}

df = pd.DataFrame(data)

# 2. Выводим первые несколько строк DataFrame
print("Первые строки таблицы:")
print(df.head())

# 3. Средняя оценка по каждому предмету
mean_scores = df.iloc[:, 1:].mean()
print("\nСредняя оценка по каждому предмету:")
print(mean_scores)

# 4. Медианная оценка по каждому предмету
median_scores = df.iloc[:, 1:].median()
print("\nМедианная оценка по каждому предмету:")
print(median_scores)

# 5. Q1 и Q3 для оценок по математике
Q1_math = df['Математика'].quantile(0.25)
Q3_math = df['Математика'].quantile(0.75)
IQR_math = Q3_math - Q1_math

print("\nQ1 для математики:", Q1_math)
print("Q3 для математики:", Q3_math)
print("IQR для математики:", IQR_math)

# 6. Стандартное отклонение
std_deviation = df.iloc[:, 1:].std()
print("\nСтандартное отклонение для каждого предмета:")
print(std_deviation)